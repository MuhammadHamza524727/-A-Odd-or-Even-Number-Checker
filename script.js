 // Prompt the user to enter a number
 let num = parseInt(prompt("Enter a number:"));

 // Check if the number is odd or even using the modulo operator
 if (num % 2 === 0) {
     alert("The number " + num + " "+ "is even.")
 } else {
     alert("The number " + num + " "+ "is odd.")

 }

 // Increment the number by 1 using the ++ operator
 num++;



 // Display the new value using an alert
 alert("After Incrementing, the number is "+" " + num + ".")